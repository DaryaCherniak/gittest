

public class Calculator {
     public static int add(int a, int b) {
        return a + b;
    }

    public static int sub(int a, int b) {
        return a - b;
    }

    public static Float div(int a, int b) {
        return (float) a / (float) b;
    }

    public static int mul(int a, int b) {
        return a * b;
    }
}
